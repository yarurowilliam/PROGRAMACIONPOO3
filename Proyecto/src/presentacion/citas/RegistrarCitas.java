        public void llenarCombo() {
        for (Medico medico : ArchivoBinario.leerDatos() ) {
            Medico m1 = new Medico(medico.getNombre(), medico.getIdentidad());
            aModel.addElement(m1);
        }
    }
        public String getMedicName(String id) {
        for (Medico m : ArchivoBinario.leerDatos()) {
            if (m.getIdentidad().equals(id)) {
                return m.getNombre();
            }
        }
        return "";
    }
    private void buscar(){
                for (Paciente paciente : ArchivoBinario.leerDatosPa()) {
            if (txtIdentidad.getText().equals(paciente.getIdentidad())) {
                try {
                    cmbTipoIden.setSelectedItem(paciente.getTipoIdentidad());
                    txtIdentidad.setText(paciente.getIdentidad());
                    txtNombreP.setText(paciente.getNombre());
                    txtApellido.setText(paciente.getApellido());
                       

                } catch (Exception ex) {
                    
                }
                
            }else{
                JOptionPane.showMessageDialog(null, "No existe esa identidad", "ERROR", JOptionPane.ERROR_MESSAGE);
                limpiar();
                
            }
        }
    
    }
    public boolean verificarmedico() {
        int cont = 0;
        for (Medico medico: ArchivoBinario.leerDatos()) {
            if (medico != null) {
                cont++;
            }
        }
        if (cont == 0) {
            return true;
        } else {
            return false; 
        }
    }
    
    public boolean verificarPaciente() {
        int cont = 0;
        for (Paciente paciente: ArchivoBinario.leerDatosPa()) {
            if (paciente != null) {
                cont++;
            }
        }
        if (cont == 0) {
            return true;
        } else {
            return false;
        }
    }
    private void registrar(){
        String mensaje;
        mensaje= Gestion.agregarCitas(cmbTipoIden.getSelectedItem().toString(), txtIdentidad.getText().trim(), txtNombreP.getText().trim(), txtApellido.getText().trim(),txtFecha.getText().trim(), txtHora.getText().trim(),cmbTipo.getSelectedItem().toString(), cmbDoctor.getSelectedItem().toString());
        JOptionPane.showMessageDialog(this, mensaje);
        limpiar();  

    }
    

   
    
    private void limpiar(){
        cmbTipoIden.setSelectedItem("SELECCIONE");
        txtIdentidad.setText("");
        txtNombreP.setText("");
        txtApellido.setText("");
        txtFecha.setText("");
        txtHora.setText("");
        cmbTipo.setSelectedItem("SELECCIONE");
        cmbDoctor.setSelectedItem("SELECCIONE");
        cmbTipoIden.requestFocus();
    }
