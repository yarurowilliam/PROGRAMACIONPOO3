    private void cargoComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cargoComboActionPerformed
        if (cargoCombo.getSelectedItem().equals("Estudiante")) {
            sisbenTexto.setEditable(true);
            sisbenTexto.setEnabled(true);
            sisbenTexto.setVisible(true);
            sisben.setVisible(true);
        }

        if (cargoCombo.getSelectedItem().equals("Docente")) {
            sisbenTexto.setEditable(false);
            sisbenTexto.setEnabled(false);
            sisbenTexto.setVisible(false);
            sisben.setVisible(false);
        }

        if (cargoCombo.getSelectedItem().equals("Funcionario")) {
            sisbenTexto.setEditable(false);
            sisbenTexto.setEnabled(false);
            sisbenTexto.setVisible(false);
            sisben.setVisible(false);
        }

        if (cargoCombo.getSelectedItem().equals("Particular")) {
            sisbenTexto.setEditable(false);
            sisbenTexto.setEnabled(false);
            sisbenTexto.setVisible(false);
            sisben.setVisible(false);
        }
    }//GEN-LAST:event_cargoComboActionPerformed

