    private void nombreTextoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nombreTextoKeyTyped
        char validar = evt.getKeyChar();
        if (Character.isDigit(validar)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Solo se permite ingresar letras");
        }
    }//GEN-LAST:event_nombreTextoKeyTyped

    private void idTextoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idTextoKeyTyped
        char validar = evt.getKeyChar();
        if (Character.isLetter(validar)) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "Solo se permite ingresar numeros");
        }
    }//GEN-LAST:event_idTextoKeyTyped

